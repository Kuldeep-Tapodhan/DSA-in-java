package BasicJava;

public class Bitwise {

    public static void main(String[] args) {
        int a=4;
        int b=12;
        System.out.println(a & b);
        System.out.println(a | b);
        System.out.println(a ^ b);
        System.out.println(a << 2);
        System.out.println(a >> 2);
        System.out.println(~b);
    }
}
